let currentLang = localStorage.getItem("lang") || "lo";
document.addEventListener("DOMContentLoaded", () => { applyLang(currentLang); });

function applyLang(lang) {
  const t = TRANSLATIONS[lang] || TRANSLATIONS["lo"];
  currentLang = lang;
  localStorage.setItem("lang", lang);

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // Update search placeholders
  ["heroSearch","exploreSearch"].forEach(id => {
    const el = document.getElementById(id);
    if (el && t["search.placeholder"]) el.placeholder = t["search.placeholder"];
  });
  const chat = document.getElementById("chatInput");
  if (chat && t["chat.placeholder"]) chat.placeholder = t["chat.placeholder"];

  // Sync dropdown button
  const m = LANG_META[lang];
  if (m) {
    const f = document.getElementById("langFlag");
    const l = document.getElementById("langLabel");
    if (f) f.textContent = m.flag;
    if (l) l.textContent = m.label;
  }

  document.querySelectorAll(".lang-option").forEach(o => o.classList.toggle("active", o.dataset.lang === lang));
  document.documentElement.lang = lang;

  // Re-render dynamic JS content
  if (typeof renderFeatured === "function") renderFeatured();
  if (typeof applyFilters === "function") applyFilters();
  if (typeof renderDetail === "function" && window._currentPlace) renderDetail(window._currentPlace);
}

function toggleLangMenu() { document.getElementById("langDropdown").classList.toggle("open"); }

function selectLang(lang, flag, label) {
  const f = document.getElementById("langFlag");
  const l = document.getElementById("langLabel");
  if (f) f.textContent = flag;
  if (l) l.textContent = label;
  document.getElementById("langDropdown").classList.remove("open");
  applyLang(lang);
}

document.addEventListener("click", e => {
  if (!e.target.closest(".lang-dropdown")) document.getElementById("langDropdown")?.classList.remove("open");
});
